  /// Translate a key with proper fallback
  static String translate(Locale locale, String key) {
    final lang = locale.languageCode;
    return _translations[lang]?[key] ?? _translations['en']?[key] ?? key;
  }

  /// Translate a crop name (returns null if not found, allowing custom formatting)
  static String? translateCrop(Locale locale, String cropKey) {
    final lang = locale.languageCode;
    final normalizedKey = cropKey.toLowerCase().replaceAll(' ', '_');
    return _cropTranslations[lang]?[normalizedKey];
  }

